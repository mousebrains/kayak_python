function updateCount() {
  var n = document.querySelectorAll('.row-cb:checked').length;
  document.getElementById('selected-count').textContent = n;
  document.getElementById('toggle-all').checked = n === data.length;
}

function toggleAll(el) {
  document.querySelectorAll('.row-cb').forEach(function(cb) { cb.checked = el.checked; });
  updateCount();
}

function selectAll() {
  document.querySelectorAll('.row-cb').forEach(function(cb) { cb.checked = true; });
  updateCount();
}

function selectNone() {
  document.querySelectorAll('.row-cb').forEach(function(cb) { cb.checked = false; });
  updateCount();
}

function selectNoData() {
  document.querySelectorAll('tr[data-id]').forEach(function(tr) {
    tr.querySelector('.row-cb').checked = tr.dataset.hasData === '0';
  });
  updateCount();
}

function selectWithData() {
  document.querySelectorAll('tr[data-id]').forEach(function(tr) {
    tr.querySelector('.row-cb').checked = tr.dataset.hasData === '1';
  });
  updateCount();
}

function esc(s){var d=document.createElement('div');d.textContent=s;return d.innerHTML;}

document.addEventListener('DOMContentLoaded', function() {
  var tbody = document.getElementById('tbody');
  data.forEach(function(r) {
    var hasData = r.gb > 0 || r.lv > 0;
    var tr = document.createElement('tr');
    if (hasData) tr.className = 'has-data';
    tr.dataset.id = r.id;
    tr.dataset.hasData = hasData ? '1' : '0';
    tr.innerHTML =
      '<td class="cb"><input type="checkbox" class="row-cb" value="' + parseInt(r.id, 10) + '" ' + (hasData ? '' : 'checked') + '></td>' +
      '<td>' + parseInt(r.id, 10) + '</td>' +
      '<td>' + esc(r.name) + '</td>' +
      '<td>' + parseInt(r.gauge, 10) + '</td>' +
      '<td>' + esc(r.aw || '') + '</td>' +
      '<td class="count">' + (r.gb || '') + '</td>' +
      '<td class="count">' + (r.lv || '') + '</td>' +
      '<td>' + r.gbd.map(function(g) { return '<span class="tag tag-gb">' + esc(g) + '</span>'; }).join(' ') + '</td>' +
      '<td>' + r.lvd.map(function(l) { return '<span class="tag tag-lv">' + esc(l) + '</span>'; }).join(' ') + '</td>';
    tbody.appendChild(tr);
  });

  document.querySelectorAll('.row-cb').forEach(function(cb) {
    cb.addEventListener('change', updateCount);
  });
  document.getElementById('toggle-all').addEventListener('change', function() { toggleAll(this); });
  document.getElementById('btn-all').addEventListener('click', selectAll);
  document.getElementById('btn-none').addEventListener('click', selectNone);
  document.getElementById('btn-no-data').addEventListener('click', selectNoData);
  document.getElementById('btn-with-data').addEventListener('click', selectWithData);

  updateCount();
});

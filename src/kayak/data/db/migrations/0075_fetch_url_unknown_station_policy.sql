-- Migration 0075: fetch_url.unknown_station_policy
--
-- Per-URL policy for a station a parser emits that has no `source` row
-- (dataset-separation S1: fetch no longer auto-creates source rows at run
-- time). Values:
--   NULL / '' / 'reject'  -> reject: the unknown station's observations are
--                            dropped (never stored, never a row created) and
--                            the fetch step exits non-zero so monitoring alerts.
--   'ignore'              -> the unknown station's observations are dropped with
--                            counts logged; the fetch step stays zero (a broad
--                            feed the operator knowingly does not fully track).
-- In every case known sibling stations on the same URL are still saved, and no
-- policy ever creates DB metadata at run time. The runtime treats any value
-- other than 'ignore' (matched case-insensitively, whitespace-trimmed) as reject
-- (fail-safe). The unknown-station path applies to multi-source URLs; a
-- single-source URL attributes any emitted station to its lone source. The
-- authoring-time
-- value check (reject anything outside {'', 'ignore', 'reject'}) lands with the
-- generate-sources/validate-dataset opt-in path in S1-fetch-2.
ALTER TABLE fetch_url ADD COLUMN unknown_station_policy TEXT;

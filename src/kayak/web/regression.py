# JSON sidecar validation
# --------------------------------------------------------------------------- #


def _reject_json_constant(name: str) -> object:
    raise UnsafeContentError(f"non-standard JSON constant {name!r}")


def validate_json_sidecar(json_text: str) -> None:
    """Validate a regression JSON fact-box (pair-linear OR leadlag schema).

    Enforces a size cap, rejects ``NaN``/``Infinity`` (which ``json.loads``
    accepts by default), requires a JSON object with a non-empty string ``slug``
    (the shared key of both schemas), and — kept deliberately loose so both the
    pair-linear and leadlag shapes pass — type-checks ``coefs``/``lags`` as lists
    when present. Raises :class:`UnsafeContentError` on any violation.
    """
    if len(json_text.encode("utf-8")) > MAX_SIDECAR_BYTES:
        raise UnsafeContentError(f"JSON sidecar exceeds {MAX_SIDECAR_BYTES} bytes")
    try:
        data = json.loads(json_text, parse_constant=_reject_json_constant)
    except (ValueError, json.JSONDecodeError) as exc:
        raise UnsafeContentError(f"JSON sidecar is not valid JSON: {exc}") from exc
    if not isinstance(data, dict):
        raise UnsafeContentError("JSON sidecar must be a JSON object")
    slug = data.get("slug")
    if not isinstance(slug, str) or not slug:
        raise UnsafeContentError("JSON sidecar must have a non-empty string 'slug'")
    for list_key in ("coefs", "lags"):
        if list_key in data and not isinstance(data[list_key], list):
            raise UnsafeContentError(f"JSON sidecar {list_key!r} must be a list")

-- Trim huc_name to the only levels any reader resolves: HUC6 + HUC8.
--
-- HUC2/4/10/12 names were 16,634 of 17,037 rows (~97%) and unread anywhere:
-- build/levels.py, build/gauges.py, huc/assign.py, gauge_picker.php, and
-- custom_gauges_handler.php all filter `level IN (6, 8)`. Reach HUC *codes*
-- come from WBD geometry (huc/assign.py load_huc12), not this name table, so
-- HUC assignment and display are unaffected. `levels assign-huc` no longer
-- writes the dropped levels either (same commit), so the trim stays trimmed.
--
-- review-3 R6.2. Idempotent: re-running deletes nothing once trimmed.
DELETE FROM huc_name WHERE level NOT IN (6, 8);

-- Migration 0045: add reach.gradient_profile column.
--
-- Holds the continuous gradient profile produced by Phase 2B of the
-- max_gradient + gradient profile work (see plans in
-- /Users/pat/.claude/plans/radiant-tinkering-fern.md, locally; or the
-- docs/PLAN_*.md copy if checked in).
--
-- Column shape: JSON text. NULL when the reach has no usable profile
-- (no geom or no DEM coverage). Populated rows look like:
--
--   {
--     "step_mi": 0.05,
--     "rmse_m": 2.4,
--     "min_drop_ft_for_significance": 33.5,
--     "samples": [
--       {"d_mi": 0.00, "lat": 44.10478, "lon": -122.02183,
--        "grad_ft_per_mi": 41.2, "w_mi": 0.5, "significant": true},
--       ...
--     ]
--   }
--
-- The sample density (step_mi) is fine enough (0.05 mi default) that the
-- PHP renderer can draw a smooth continuous line without further
-- client-side smoothing. Per-sample lat/lon piggy-back so the chart can
-- sync a cursor-position map marker.
--
-- The matching SQLAlchemy column declaration lands in
-- src/kayak/db/models.py in the same commit — without that, fresh
-- `levels init-db` would create a Reach table that lacks this column
-- (since init-db rebuilds via Base.metadata.create_all()).
--
-- A second migration (0046_reach_max_gradient_backfill.sql) follows in
-- a later commit, generated by scripts/emit_max_gradient_migration.py
-- from the local DB after Phase 2B --apply, to populate the column with
-- per-row UPDATEs.

ALTER TABLE reach ADD COLUMN gradient_profile TEXT;
